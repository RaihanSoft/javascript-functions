// ! mile to kilometer 


function MiKi(mile){

    const result = mile * 1.60934 ;
    return result

}
const mile = 10 ;
const total = MiKi(mile)
console.log(total);

// ! kilometer to mile

function KiMi (kilo){
    const result = kilo * 0.621371;
   return result

}
const totals = KiMi(10)
console.log(totals);